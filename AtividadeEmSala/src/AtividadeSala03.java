public class AtividadeSala03 {
    public static void main(String[] args){
        int a = 15;
        int b = 20;
        int resultado = 0;

        resultado = a + b;
        System.out.println("a + b:" + resultado );
        resultado = a - b;
        System.out.println("a - b:" + resultado);
        resultado = a / b;
        System.out.println("a / b:" + resultado);
        resultado = a * b;
        System.out.println("a * b:" + resultado);

        System.out.println("a == b ? " + (a == b));
        System.out.println("a != b ? " + (a != b));
        System.out.println("a >= b ? " + (a >= b));
        System.out.println("a =< b ? " + (a <= b));

    }
}
